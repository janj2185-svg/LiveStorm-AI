export type TranslationKey = keyof typeof translations.en;

export function getLanguageDir(lang: Language): "ltr" | "rtl" {
  const found = LANGUAGES.find((l) => l.code === lang);
  return found?.dir ?? "ltr";
}

export function detectLanguage(): Language {
  const browserLang = (navigator.language ?? "en").split("-")[0].toLowerCase();
  const fullLang = (navigator.language ?? "en").toLowerCase();

  if (fullLang.startsWith("zh-tw") || fullLang.startsWith("zh-hant")) return "zh-TW";

  const supported: Language[] = [
    "en", "uk", "pl", "de", "fr", "es", "it", "pt",
    "nl", "tr", "ar", "hi", "ja", "ko", "zh", "id", "vi", "th", "ru",
  ];
  return supported.includes(browserLang as Language) ? (browserLang as Language) : "en";
}

export function getStoredLanguage(): Language {
  try {
    const stored = localStorage.getItem("livestorm_ui_language");
    if (stored) {
      const allCodes: Language[] = [
        "en", "uk", "pl", "de", "fr", "es", "it", "pt",
        "nl", "tr", "ar", "hi", "ja", "ko", "zh", "zh-TW",
        "id", "vi", "th", "ru",
      ];
      if (allCodes.includes(stored as Language)) return stored as Language;
    }
  } catch {
    // ignore
  }
  return detectLanguage();
}

export function setStoredLanguage(lang: Language): void {
  try {
    localStorage.setItem("livestorm_ui_language", lang);
  } catch {
    // ignore
  }
}
