export type TranslationKey = keyof typeof translations.en;

export function detectLanguage(): Language {
  const browserLang = (navigator.language ?? "en").split("-")[0].toLowerCase();
  const supported: Language[] = ["en", "uk", "pl", "de"];
  return supported.includes(browserLang as Language) ? (browserLang as Language) : "en";
}

export function getStoredLanguage(): Language {
  try {
    const stored = localStorage.getItem("livestorm_ui_language");
    const supported: Language[] = ["en", "uk", "pl", "de"];
    if (stored && supported.includes(stored as Language)) return stored as Language;
  } catch {
    // ignore
  }
  return detectLanguage();
}

export function setStoredLanguage(lang: Language): void {
  try {
    localStorage.setItem("livestorm_ui_language", lang);
  } catch {
    // ignore
  }
}
